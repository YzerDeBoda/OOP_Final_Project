    public void getSum() {
        double sum = 0;
        for (int i = 0; i < tableValues.getRowCount(); i++) {
            String pricesString = tableValues.getValueAt(i, 2).toString();
            String pricesStringConvertion = pricesString.replace(" pesos", "");
            double prices = Double.parseDouble(pricesStringConvertion);
            sum += prices;
            }
        double sum1 = 0;
            for (int j = 0; j < tableValues.getRowCount(); j++) {
                String pricesString = tableValues.getValueAt(j, 3).toString();
                String pricesStringConvertion = pricesString.replace(" pesos", "");
                double prices = Double.parseDouble(pricesStringConvertion);
                sum1 += prices;
                }
            
                String formattedSum = String.format("%.4f", sum);
                String sumString = String.valueOf(formattedSum + " pesos");
                
                String formattedSum1 = String.format("%.4f", sum1);
                String sumString1 = String.valueOf(formattedSum1 + " pesos");
                
                String usedAppliance = String.valueOf(tableValues.getRowCount() + " Appliances");
                
                totalSum ts = new totalSum(); 
                ts.dayEstimation.setText(sumString);
                ts.monthEstimation.setText(sumString1);
                ts.appliancesUsed.setText(usedAppliance);
                ts.setVisible(true);
}
