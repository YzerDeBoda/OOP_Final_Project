    private void statsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statsButtonActionPerformed
        TableModel tableModel = tableValues.getModel();

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            double sum = 0;
            String appliance = (String) tableModel.getValueAt(i, 0);
            String pricePerUse = (String) tableModel.getValueAt(i, 2);
            String pricePerUseRemoved = pricePerUse.replace(" pesos", "");
            double pricePerUseNew = Double.parseDouble(pricePerUseRemoved);
            sum += pricePerUseNew;
            
        dataset.addValue(sum, "Price (Day)", appliance);
        }
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            double sum = 0;
            String appliance = (String) tableModel.getValueAt(i, 0);
            String pricePerUse = (String) tableModel.getValueAt(i, 3);
            String pricePerUseRemoved = pricePerUse.replace(" pesos", "");
            double pricePerUseNew = Double.parseDouble(pricePerUseRemoved);
            sum += pricePerUseNew;
            
        dataset.addValue(sum, "Price (Month)", appliance);
        }
        JFreeChart chart = ChartFactory.createBarChart(
            "Appliance Usage Statistics",
            "",
            "Price (Pesos)",
            dataset,
            PlotOrientation.VERTICAL,
            true, true, false
        );
        
        CategoryPlot plot = chart.getCategoryPlot();

        BarRenderer renderer = (BarRenderer) plot.getRenderer();

        renderer.setSeriesPaint(0, new Color(147,225,111));
        renderer.setSeriesPaint(1, new Color(255,204,0));

        ChartFrame frame = new ChartFrame("Statistics", chart);
        frame.setPreferredSize(new java.awt.Dimension(560, 367));
        frame.setVisible(true);
        frame.setSize(450, 350);
        frame.setLocationRelativeTo(null);
        
    }//GEN-LAST:event_statsButtonActionPerformed

